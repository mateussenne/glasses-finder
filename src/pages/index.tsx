import {
  Button,
  Center,
  Container,
  FileButton,
  Grid,
  Title,
} from "@mantine/core";
import { useForm } from "@mantine/form";
import { signIn, signOut, useSession } from "next-auth/react";
import Head from "next/head";
import { useMemo, useRef, useState } from "react";

export default function Home() {
  const [file, setFile] = useState<File | null>(null);
  const form = useForm({
    initialValues: {
      file: null,
    },
  });

  const photoUploadForm = useRef<HTMLFormElement>(null);

  useMemo(() => {
    setFile(form.values.file);
    async function uploadFile() {
      if (file) {
        const formData = new FormData();
        formData.append("file", file);
        await fetch("/api/uploads/file-upload", {
          method: "POST",
          body: formData,
        });
      }
    }

    // eslint-disable-next-line @typescript-eslint/no-floating-promises
    uploadFile();
  }, [form.values.file, file]);

  return (
    <>
      <Head>
        <title>Send your photo</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#1c2046] to-[#18140b]">
        <Container>
          <Grid grow>
            <Grid.Col span={12}>
              <Center>
                <Title className="font-sans text-7xl text-white">
                  Send your photo
                </Title>
              </Center>
            </Grid.Col>
            <Grid.Col span={6}>
              <form action={"/api/uploads/file-upload"} ref={photoUploadForm}>
                <FileButton
                  accept="image/png,image/jpeg"
                  {...form.getInputProps("file")}
                >
                  {(props) => (
                    <Button
                      className="float-right rounded-full bg-white/10 px-10 py-3 font-semibold text-white no-underline transition hover:bg-amber-950"
                      {...props}
                    >
                      Upload image
                    </Button>
                  )}
                </FileButton>
              </form>
            </Grid.Col>
            <Grid.Col span={6}>
              <Button className="float-left rounded-full bg-white/10 px-10 py-3 font-semibold text-white no-underline transition hover:bg-amber-950">
                Take a photo
              </Button>
            </Grid.Col>
            <Grid.Col span={12}>
              <form onSubmit={form.onSubmit((values) => console.log(values))}>
                <Center>
                  <Button
                    className="rounded-full bg-white/10 px-10 py-3 font-semibold text-white no-underline transition hover:bg-amber-950"
                    type="submit"
                  >
                    Send!
                  </Button>
                </Center>
              </form>
            </Grid.Col>
          </Grid>
        </Container>
      </main>
    </>
  );
}

function AuthShowcase() {
  const { data: sessionData } = useSession();

  return (
    <div className="flex flex-col items-center justify-center gap-4">
      <p className="text-center text-2xl text-white">
        {sessionData && <span>Logged in as {sessionData.user?.name}</span>}
      </p>
      <button
        className="rounded-full bg-white/10 px-10 py-3 font-semibold text-white no-underline transition hover:bg-white/20"
        onClick={sessionData ? () => void signOut() : () => void signIn()}
      >
        {sessionData ? "Sign out" : "Sign in"}
      </button>
    </div>
  );
}
